def main():
    application = Application.builder().token("TOKEN").build()

    application.add_handler(CommandHandler("start", start))
    application.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_reply_keyboard))

    # –û–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ –ø—Ä–∏–≤—ã—á–µ–∫
